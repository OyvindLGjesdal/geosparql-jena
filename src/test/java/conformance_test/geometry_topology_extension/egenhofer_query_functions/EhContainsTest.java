public class EhContainsTest {

    @BeforeClass
    public static void setUpClass() {
        /**
         * Initialize all the topology functions.
         */
        RegistryLoader.load();
        infModel = initWktModel();
    }
    private static InfModel infModel;

    @AfterClass
    public static void tearDownClass() {
    }

    @Before
    public void setUp() {

    }

    @After
    public void tearDown() {

    }

    @Test
    public void positiveTest() {

        /**
         * ehContains is slightly different from the sfContains, which will not
         * return the same instance while the sfContains will return the same
         * instance.
         */
        ArrayList<String> expectedList = new ArrayList<>();
        expectedList.add("http://example.org/ApplicationSchema#C");

        ArrayList<String> actualList = resourceQuery(geometryTopologyQuery("geof:ehContains", "Point(-83.4 34.4)"), infModel);
        assertEquals(expectedList, actualList);
    }

    @Test
    public void negativeTest() {

        assertFalse(emptyQuery(geometryTopologyQuery("geof:ehContains", "Point(-86.4 31.4)"), infModel));
    }

}
